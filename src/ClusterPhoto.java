
public class ClusterPhoto {
	public int photoId;
	public String url;
	public String date;
	public String username;
	public String description;
	
	public ClusterPhoto (int photoId, String url, String date, String username, String description) {
		this.photoId = photoId;
		this.url = url;
		this.date = date;
		this.username = username;
		this.description = description;
	}
}
